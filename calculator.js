// Define the numbers and the operation here
let num1 = 10;
let num2 = 5;
let theopration = "-";
// Write if-statements here
if (theopration === "-") {
  console.log(num1 - num2);
} else if (theopration === "*") {
  console.log(num1 * num2);
} else if (theopration === "/") {
  console.log(num1 / num2);
} else if (theopration === "+") {
  console.log(num1 + num2);
} else {
  console.log("No Equation");
}
